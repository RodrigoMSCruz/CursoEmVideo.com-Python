# Calcula a média aritmétrica de 2 notas que são inseridas pelo usuário.
nota1 = float(input('Digite a primeira nota do aluno: '))
nota2 = float(input('Digite a segunda nota do aluno: '))
media = (nota1 + nota2) / 2
print('A 1ª nota  é: {}.\nA 2ª nota é : {}.\nE a média é {}'.format(nota1, nota2, media))
