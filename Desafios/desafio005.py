# Lê o número inteiro que o usuário digita e mostra o anterior e o posterior a ele.
n = int(input('Digite um número inteiro: '))
ant = n - 1
suc = n + 1
print('O número é {}. \nSeu antecessor é: {} \ne seu sucessor é: {}'.format(n, ant, suc))
